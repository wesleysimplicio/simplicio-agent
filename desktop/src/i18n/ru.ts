// Russian (ru) CLDR cardinal plural categories for non-negative integers:
//  - one:  n % 10 == 1 AND n % 100 != 11        (1, 21, 31, 101 …)
//  - few:  n % 10 in [2,3,4] AND n % 100 not in [12,13,14]  (2, 3, 4, 22 …)
//  - many: everything else                       (0, 5-20, 25 …)
function pluralRu(n: number, one: string, few: string, many: string): string {
  const mod10 = n % 10
  const mod100 = n % 100
  if (mod10 === 1 && mod100 !== 11) return one
  if (mod10 >= 2 && mod10 <= 4 && (mod100 < 12 || mod100 > 14)) return few
  return many
}

