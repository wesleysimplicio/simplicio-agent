// Polish (pl) CLDR cardinal plural rule for non-negative integers:
//  - one:  n == 1
//  - few:  n % 10 in [2,3,4] AND n % 100 not in [12,13,14]
//  - many: everything else (0, 5-21, 22-24 wrap differently, etc.)
function pluralPl(n: number, one: string, few: string, many: string): string {
  if (n === 1) return one
  const mod10 = n % 10
  const mod100 = n % 100
  if (mod10 >= 2 && mod10 <= 4 && (mod100 < 12 || mod100 > 14)) return few
  return many
}

